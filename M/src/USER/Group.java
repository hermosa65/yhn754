package USER;

public class Group {
	
	private String USER_GROUP_ID;
	private String USER_GROUP_NAME;
	private String USER_GROUP_LEVEL;
	private String DESCRIPTION;
	private String DISPLAY_ORDER;
	public String getUSER_GROUP_ID() {
		return USER_GROUP_ID;
	}
	public void setUSER_GROUP_ID(String uSER_GROUP_ID) {
		USER_GROUP_ID = uSER_GROUP_ID;
	}
	public String getUSER_GROUP_NAME() {
		return USER_GROUP_NAME;
	}
	public void setUSER_GROUP_NAME(String uSER_GROUP_NAME) {
		USER_GROUP_NAME = uSER_GROUP_NAME;
	}
	public String getUSER_GROUP_LEVEL() {
		return USER_GROUP_LEVEL;
	}
	public void setUSER_GROUP_LEVEL(String uSER_GROUP_LEVEL) {
		USER_GROUP_LEVEL = uSER_GROUP_LEVEL;
	}
	public String getDESCRIPTION() {
		return DESCRIPTION;
	}
	public void setDESCRIPTION(String dESCRIPTION) {
		DESCRIPTION = dESCRIPTION;
	}
	public String getDISPLAY_ORDER() {
		return DISPLAY_ORDER;
	}
	public void setDISPLAY_ORDER(String dISPLAY_ORDER) {
		DISPLAY_ORDER = dISPLAY_ORDER;
	}
	
}
	
	